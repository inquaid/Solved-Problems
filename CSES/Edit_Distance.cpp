vector<vi> dp;

int f(int i, int j) {
  if(i == 0 or j == 0) {
    return abs(i - j);
  }
  if(dp[i][j] != -1) return dp[i][j];
  if(a[i - 1] == b[j - 1]) {
    return f(i - 1, j - 1);
  }
  return dp[i][j] = 1 + min({f(i, j - 1), f(i - 1, j), f(i - 1, j - 1)});
}

int f2(int n, int m) {
  dp[0][0] = 0;
  for (int i = 0; i <= n; ++i) {
    for (int j = 0; j <= m; ++j) {
      if(i == 0 or j == 0) {
        dp[i][j] = abs(i - j);
        continue;
      }
      if(a[i - 1] == b[j - 1]) {
        dp[i][j] = dp[i-1][j-1];
      } else
        dp[i][j] = 1 + min({dp[i][j - 1], dp[i - 1][j], dp[i - 1][j - 1]}); 
    }
  } return dp[n][m];
}

