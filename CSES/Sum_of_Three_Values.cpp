pii two_sum(vector<pii> &a, int i, int j, int target) {

  while(i < j) {
    int sum = a[i].ff + a[j].ff;
    if(sum == target) return {a[i].ss, a[j].ss};
    else if(sum < target) {
      i++;
    } else j--;
  }

  return {-1, -1};
}

